<h2>What is Unravel?</h2>
<p>Unravel is a site to share meanings for songs as well as a great place to comment on songs, post journals, and make friends.  This site
is meant to be as open and friendly as possible to people from all walks of life and all genres of music.</p>

<p>We aim to have the most accurate and highest volume of song meanings on the Internet.  We plan on doing this by having you, the users,
suggest meanings for songs and vote on the meanings you think most closely represents the meaning of the song.</p>